public class SwitchTest1 {

    public static void main(String[] args) {
        int x = 2;

        switch (x) {
            default:
                System.out.println("default");
            case 2:
                System.out.println("2");

            case 3:
                System.out.println("3");
            case 4:
                System.out.println("4");

        }
    }
}
